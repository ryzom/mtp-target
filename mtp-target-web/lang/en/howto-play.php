<center><h2>How to Play</h2></center>

<h3>Launching the game</h3>
<ul>
<li>Get the <a href="http://mtptarget.free.fr/download">install here</a>.</li>
<li>Install and run mtp-target</li>
<li>Enter a login and a password, the first time you login, these informations will be stored into the database<br>
<b>Warning</b> : login size is <b>20 characters max</b>, and only thoses chararters are allowed <b>a-z A-Z 0-9 [ ] _ -</b>
</li>
<li>Now you have to choose one of the available server</li>
<li>The game could be automaticaly patched now.</li>
<li>Once you are into the game you have to wait the current session ends (60 seconds max).</li>
</ul>


<h3>Playing The game</h3>
<ul>
<li>At the end of the final count down the session starts.</li>
<li>On the floor, use <b>RIGHT</b> and <b>LEFT</b> arrows to turn, <b>UP</b> and <b>DOWN</b> keys control the speed.</li>
<li>Once you are in the air press <b>CONTROL</b> key to switch to flying mode.</li>
<li>In flying mode <b>UP</b> arrow increase your altitude but decrease your speed.</li>
<li>In flying mode <b>DOWN</b> arrow decrease altitude but increase speed.</li>
<li><b>Be carreful</b> Don't touch target in flying mode</li>
<li>Near the target press again <b>CONTROL</b> key to switch back to the normal mode.</li>
<li>When every body has land off, the session ends, and you can see your score.</li>
</ul>

<h3>Tips</h3>
<ul>
<li>If you are not busy try <b>F9</b> and <b>F10</b> keys to see other players, <b>F11</b> to go back to your pingoo.</li>
<li>Look at the right bar to know you current altitude.</li>
</ul>


<h3>The keys</h3>
<ul>
<li><b>F2</b> : Make a screenshot</li>
<li><b>F9</b> : Watch next player</li>
<li><b>F10</b> : Watch previous player</li>
<li><b>F11</b> : Watch your player</li>
<li><b>TAB</b> : Display scores</li>
<li><b>ESC</b> : Back to <i>OS</i> world :(</li>
<li><b>Other keys</b> : Chat with other players</li>
<li><b>ENTER</b> : Send your sentence to the chat</li>
</ul>



