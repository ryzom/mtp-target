<center><h2>Commands list.</h2></center><br/>
<br/>

<b>User</b> commands :
<ul>
<li><b>/voteMap levelName</b><br/>
In game, you can type <b>/voteMap</b> levelName to vote for the level called levelName.A vote need 33% of players voting the same level to be accepted.
A list of levels can be found <b><a href="?page=stat_map_list"> here</a></b>.<br/>
When you use <b>/voteMap</b> command, you can use only a part of the name, for ex, if you want to vote for "the wall" map, you can use this command line :<br/>
<b>/voteMap wall</b><br/>
there is alos a short version of the command using only <b>/v</b> :<br/>
<b>/v wall</b><br/>
</li><br/>

<li><b>/help</b><br/>
Display commands list.
</li><br/>
</ul>


<b>Moderator</b> commands :
<ul>

<li><b>/playerlist</b><br/>
Display player list like this : id#userName<br/>
In any command you can use <i>id</i> instead of <i>userName</i>
</li><br/>

<li><b>/kick userName</b><br/>
kick the user from current server.
</li><br/>

<li><b>/ban userName [duration]</b><br/>
ban the user during [duration] minutes (default 10 minutes).
User cannot join any server when he is banned.
</li><br/>

<li><b>/mute userName</b><br/>
Switch on/off user chat. Use once to mute a user, use it once again to let him speak again.
</li><br/>

<li><b>/forceEnd</b><br/>
Force the current game to end.
</li><br/>

<li><b>/forceMap userName</b><br/>
Force the next level, overriding any /voteMap call
</li><br/>

<li><b>/reset</b><br/>
Reset current game and start immediatly a new one (like forceEnd but does'nt save game score)<br/>
<b>Unstable</b>.
</li><br/>

<li><b>/report userName info</b><br/>
Send a report about user to the admins.
</li><br/>

</ul>

