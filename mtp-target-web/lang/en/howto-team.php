<center><h2>How to join/create a team.</h2></center><br/>
<br/>
Very easy, you just have to add [myteam] before your nick name.<br/>
For exemple, if I'm in the <b>dev</b> team and my nick is skeet, I have to name my account <b>[dev]</b>skeet.<br/>
That's all, no user limit or anything else.<br/>
What do you get being in a team ? Almost be sure to play together in the <i>team</i> map.<br/>

Note : you can change yout account name in the <i>settings</i> part when your are logged on the web site.<br>
