<center><h2>Comment creer ou rejoindre une equipe.</h2></center><br/>
<br/>
C'est facile, il suffit d'ajouter [mateam] devant son nom.<br/>
Par exemple si je suis dans la team <b>dev</b> et que je m'appelle skeet ca fait <b>[dev]</b>skeet<br/>
Et voila c'est pas plus compliqué que ca, il n'y pas de limite ou autre.<br/>
Qu'est ce qu'on gagne ? Etre quasi sure de jouer ensemble sur la map en equipe.<br/>

Precision : on peut changer de nom dans la rubrique <i>vos réglages</i> en etant loggué sur le site.<br/>
