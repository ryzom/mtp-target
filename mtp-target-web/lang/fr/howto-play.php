<center><h2>Comment jouer</h2></center>

<h3>Lancer le jeu</h3>
<ul>
<li>récuperez <a href="http://mtptarget.free.fr/mtp-target.exe">le programme d'installation</a>.</li>
<li>lancer <code>mtptarget_launcher.exe</code>.</li>
<li>Entrez un login et un mot de passe, la premiere fois que vous vous logger, ces informations sont enregistrées dans la base de donnée</li>
<li>Mantenant choisissez un des serveurs disponible</li>
<li>Le jeu peut etre automatiquement patché</li>
<li>Une fenetre <code>"Press enter to connect to the server"</code> devrai apparaitre, appuyer sur <b>ENTER</b> :).</li>
<li>Une fois dans le jeu, vous devez attendre que la session courante s'acheve (60 seconds maxi).</li>
</ul>


<h3>Jouer</h3>
<ul>
<li>A la fin du compte-a-rebourd la session débute</li>
<li>Sur le sol, utilisez les fleches <b>DROITE</b> et <b>GAUCHE</b> pour tourner, ainsi que les touches <b>HAUT</b> et <b>BAS</b> pour controler votre vitesse.</li>
<li>Une fois en l'air appuyer sur <b>CONTROL</b> pour passer en mode vole.</li>
<li>En mode vole : la touche <b>HAUT</b> augmente votre altitude mais diminue votre vitesse.</li>
<li>En mode vole : la touche <b>BAS</b> diminue votre altitude mais augmente votre vitesse.</li>
<li><b>Atention</b> ne touchez pas la cible en mode vole</li>
<li>Ue fois proche de la cible appuyez sur <b>CONTROL</b> pour repasser en mode roule.</li>
<li>Une fois que tous le monde a atteris, la session se termine, et vous pouvez voire votre score.</li>
</ul>

<h3>Tips</h3>
<ul>
<li>Si vous avez le temps appuyer sur <b>F9</b> et <b>F10</b> pour voire les autres joueurs, <b>F11</b> pour revenir à votre personage.</li>
<li>Regardez votre altitude sur la barre de droite.</li>
</ul>


<h3>Les touches</h3>
<ul>
<li><b>F1</b> : Affiche les boites de physique (debug info)</li>
<li><b>F2</b> : Fait une capture d'ecran</li>
<li><b>F4</b> : Affiche les info de debug</li>
<li><b>F5</b> : Termine la session courante (seulement en etant admin)</li>
<li><b>F6</b> : Reset la session courante (seulement en etant admin)</li>
<li><b>F9</b> : Vue sur le joueur suivant</li>
<li><b>F10</b> : Vue sur le joueur precedent</li>
<li><b>F11</b> : Vue sur votre joueur</li>
<li><b>TAB</b> : Affiche les scores</li>
<li><b>ESC</b> : Retour au systeme d'exploitation :(</li>
<li><b>Other keys</b> : Chat avec les autres joueurs</li>
<li><b>ENTER</b> : Envoye votre phrase sur le chat</li>
</ul>



