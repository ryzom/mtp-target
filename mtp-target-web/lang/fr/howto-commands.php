<center><h2>Liste des commandes.</h2></center><br/>
<br/>

Commandes pour les <b>Joueurs</b> :
<ul>
<li><b>/voteMap nomDuLevel</b><br/>
Vous pouvez, dans le jeu, taper la commande <b>/voteMap nomDuLevel</b> pour voter pour le niveau appelé nomDuLevel
Un vote necessite que 33% des joueurs vote pour le meme niveau pour aboutir.
La liste des niveaux se trouve <b><a href="?page=stat_map_list">ici</a></b>.<br>
Quand vous voulez voter pour un niveau, vous pouvez utiliser qu'une partie du nom du niveau, par ex : pour voter pour "the wall", vous pouvez taper :<br/>
<b>/voteMap wall</b>
il y a aussi une version courte, <b>/v</b> :<br/>
<b>/v wall</b>
</li><br/>

<li><b>/help</b><br/>
Affiche la liste de commandes.
</li><br/>
</ul>


Commandes pour les <b>Moderateurs</b> :
<ul>

<li><b>/playerlist</b><br/>
Affiche la liste des joueur, comme ceci : id#NomJoueur<br/>
Dans toutes les commandes vous pouvez utiliser <i>id</i> a la place du <i>NomJoueur</i>
</li><br/>

<li><b>/kick NomJoueur</b><br/>
Expulse le joueur du serveur.
</li><br/>

<li><b>/ban NomJoueur [temps]</b><br/>
Empeche le joueur de se connecter a tous les serveur pendant une durée de [temps] minutes(10 minutes par defaut).<br/>
</li><br/>

<li><b>/mute NomJoueur</b><br/>
Active/désactive la possibilité d'ecrire.
Utiliser une fois pour rendre un joueur silencieux, une autre fois pour le laisser parler a nouveau.
</li><br/>

<li><b>/forceEnd</b><br/>
Force la fin du niveau.
</li><br/>

<li><b>/forceMap NomDuNiveau</b><br/>
Force le nom du prochain niveau, prioritaire sur les appelle a /voteMap.
</li><br/>

<li><b>/reset</b><br/>
Termine immediatement le niveau courant, et en charge un nouveau.(comme forceEnd mais ne sauve pas le score) <br/>
<b>Pas tres stable</b>.
</li><br/>

<li><b>/report NomJoueur info</b><br/>
Envoi aux admins, un rapport sur le joueur.
</li><br/>

</ul>

